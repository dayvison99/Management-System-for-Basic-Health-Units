     private void preencheTabela(List<Agendamento> lista){
        DefaultTableModel modelo = new DefaultTableModel();
       
        modelo.addColumn("Paciente");
        modelo.addColumn("Medico");
        modelo.addColumn("Unidade");
        modelo.addColumn("Periodo");
        modelo.addColumn("Data");
        
        for(Agendamento a : lista){
            Vector linha = new Vector();
            linha.add(a.getPaciente());
            linha.add(a.getMedico());
            linha.add(a.getUnidadeSaude());
            linha.add(a.getTurno().toString());
            linha.add(a.getData().toString());
           
            
            modelo.addRow(linha);
        }
        
        tblListagem.setModel(modelo);
    }
     public void editarAgendamento(int id){
        Agendamento entidade;
        
        if(id == 0)
            
            entidade = new Agendamento(id,null,null,null,null,new Date(),null);
        else
            entidade = dao.Abrir(id);
        
        editar = new TelaAgendamentoEditar();
        
        editar.setEntidade(entidade);
        
        editar.setListagem(this);
        
        this.getParent().add(editar);
        
        editar.setVisible(true);
        
        this.setVisible(false);
    }
     
    public void buscarPaciente(Date nome){
        
        Agendamento filtro = new Agendamento (0, null, null, null,null, nome,null);
        
        List<Agendamento> busca = dao.Buscar(filtro);
        
        preencheTabela(busca);
        }
     
     

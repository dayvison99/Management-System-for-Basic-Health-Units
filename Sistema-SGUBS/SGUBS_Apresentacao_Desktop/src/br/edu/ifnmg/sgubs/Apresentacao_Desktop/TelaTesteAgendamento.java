    public void preecheMedicos() {
        
       conex.conexao();
        
       conex.excultaSQL("select * from medico ");
        
        try {
            conex.rs.first();
            cbxUnidade.removeAllItems();
            
            do{
                cbxUnidade.addItem(conex.rs.getString("nome"));
            }while(conex.rs.next());
                                 
         } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "erro"+ex.getMessage());
        }
        
        conex.desconecta();
    }
        
        
         
        
    
